"""
Reps
while
Performs an action while a condition is true
"""

condition = True

while condition:
    name = input("What's your name? ").capitalize()

    if name == "Jean":
        print("Little gay.")
    elif name == 'Lucas':
        print("Pure white and German.")
    elif name == 'Enzo':
        print("God's city")
    else:
        print("I do not know that one.")
